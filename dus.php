        <h2>Neuer Zollhof</h2><br/><br/><h3 class="h3-alt">Düsseldorf, Germany (1998)</h3><br/><br/><br/>
        <p class="uppercase">Neuer Zollhof or Der Neue Zollhof (The New Zollhof), located at Neuer Zollhof 2-6, Unterbilk, is a prominent landmark of Media Harbor, part of the redeveloped port of Düsseldorf, Germany.
 </p><br/>
        <p>The building complex consisting of three separate buildings, was designed by American architect Frank O. Gehry and completed in 1998. Floorplans and facades of all three buildings curve and lean, reason for them being likened to leaning towers. The tallest building is 14 stories high and just under 50 m tall. Each building has a different facade cladding - the outer two in white plaster and red brick respectively; the central building's stainless steel facade reflects material and shapes of its two neighbour buildings.</p><br/>
<p>The buildings have a total gross floor area of 29.000 square metres. A previous architectural design competition for the site was won by British architect Zaha Hadid during the early 1990s, however never commissioned.</p><br/>
    <p>The buildings are also popular with tourists who visit the region.</p><br/>
